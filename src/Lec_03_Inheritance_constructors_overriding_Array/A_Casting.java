
package Lec_03_Inheritance_constructors_overriding_Array;


public class A_Casting {
    
    public static void main(String args[]){
        
        int n = 12;
        double m = 34.0;
        
        //auto casting
        double d = n;
        System.out.println("Integer to Double : "+n);
        
        //manuel casting
        int mm = (int) m;
        System.out.println("Double to Integer : "+mm);
        
        
        
    }
    
}
